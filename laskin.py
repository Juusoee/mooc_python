luku1 = int(input("Luku1: "))
luku2 = int(input("Luku1: "))
komento = input("Komento: ")
 
if komento == "summa":
    print(f"{luku1} + {luku2} = {luku1 + luku2}")
if komento == "tulo":
    print(f"{luku1} * {luku2} = {luku1 * luku2}")
if komento == "erotus":
    print(f"{luku1} - {luku2} = {luku1 - luku2}")